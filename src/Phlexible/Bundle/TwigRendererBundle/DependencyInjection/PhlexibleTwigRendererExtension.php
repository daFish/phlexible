namespace Phlexible\Bundle\TwigRendererBundle\DependencyInjection;

use Symfony\Component\Config\FileLocator;
use Symfony\Component\DependencyInjection\ContainerBuilder;
use Symfony\Component\DependencyInjection\Loader\YamlFileLoader;
use Symfony\Component\HttpKernel\DependencyInjection\Extension;

/**
 * Twig renderer extension
 *
 * @author Stephan Wentz <sw@brainbits.net>
 */
class PhlexibleTwigRendererExtension extends Extension
{
    /**
     * {@inheritdoc}
     */
    public function load(array $config, ContainerBuilder $container)
    {
        $loader = new YamlFileLoader($container, new FileLocator(__DIR__.'/../Resources/config'));
        $loader->load('services.yml');

        $container->setAlias('phlexible_twig_renderer.twig.loader', 'phlexible_twig_renderer.twig.loader.filesystem');
    }
}
